#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	ptr = (void *)malloc(nmemb * size);
	if (!ptr || nmemb == 0 || size == 0)
		return (NULL);
	bzero(ptr, nmemb);
	return (ptr);
}
